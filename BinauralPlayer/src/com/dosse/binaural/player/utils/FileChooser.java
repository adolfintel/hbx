    //return an Object[2]: first element is the actual file; second element is the file filter selected by the user (from the FileFilter[] array)

    public static Object[] show(FileFilter[] ff, boolean openDialog) {
        FileChooser dialog = new FileChooser(new javax.swing.JFrame(), true, ff, new Point(Toolkit.getDefaultToolkit().getScreenSize().width / 2, Toolkit.getDefaultToolkit().getScreenSize().height / 2), openDialog);
        dialog.setVisible(true);
        while (!dialog.done) {
            try {
                Thread.sleep(100);
            } catch (InterruptedException ex) {
                Logger.getLogger(FileChooser.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        return new Object[]{dialog.selectedFile, dialog.selectedFF};
    }
