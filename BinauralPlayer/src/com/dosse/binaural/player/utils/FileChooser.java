    private void jFileChooser1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jFileChooser1ActionPerformed
        if (evt.getActionCommand().equals(JFileChooser.APPROVE_SELECTION)) {
            selectedFile = jFileChooser1.getSelectedFile();
            if (jFileChooser1.getDialogType() == JFileChooser.SAVE_DIALOG && selectedFile != null && selectedFile.exists()) {
                setAlwaysOnTop(false);
                int sel = YesNoCancelDialog.confirm(overwriteConfirm, title.getText(), true);
                setAlwaysOnTop(true);
                if (sel == YesNoCancelDialog.NO) {
                    selectedFile = null;
                    return;
                }
                if (sel == YesNoCancelDialog.CANCEL) {
                    selectedFile = null;
                }
            }
            if (selectedFile != null) {
                lastPath = selectedFile.getParent();
            }
            selectedFF = jFileChooser1.getFileFilter();
        }
        done = true;
        if (Main.DISABLE_TRANSLUCENCY) {
            dispose();
            return;
        }
        setOpacity(1);
        for (float i = 1; i >= 0; i -= 0.1) {
            setOpacity(i);
            try {
                Thread.sleep(20);
            } catch (InterruptedException ex) {
            }
        }
        setOpacity(0);
        dispose();
    }//GEN-LAST:event_jFileChooser1ActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        if (Main.DISABLE_TRANSLUCENCY) {
            return;
        }
        new Thread() {
            public void run() {
                for (float i = 0; i <= 1; i += 0.1) {
                    setOpacity(i);
                    try {
                        Thread.sleep(20);
                    } catch (InterruptedException ex) {
                    }
                }
                setOpacity(1);
            }
        }.start();
    }//GEN-LAST:event_formWindowOpened

