    private int selection=-1;
    
    public static int confirm(String message, String title, boolean cancelable) {
        YesNoCancelDialog dialog = new YesNoCancelDialog(new javax.swing.JFrame(), true, "<html>" + message + "</html>", "<html>" + title + "</html>", new Point(Toolkit.getDefaultToolkit().getScreenSize().width / 2, Toolkit.getDefaultToolkit().getScreenSize().height / 2),cancelable);
        dialog.setVisible(true);
        while(!dialog.done){
            try {
                Thread.sleep(100);
            } catch (InterruptedException ex) {
                Logger.getLogger(YesNoCancelDialog.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        return dialog.selection;
    }

